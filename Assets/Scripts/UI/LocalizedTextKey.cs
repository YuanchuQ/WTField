// 定义可本地化 UI 文本的键
// 标识界面文本对应的本地化条目
public enum LocalizedTextKey
{
    // 主标题
    Title,
    // 开始按钮
    Start,
    // 暂停标题
    Paused,
    // 暂停提示
    PauseHint,
    // 继续按钮
    Resume,
    // 结算胜利
    Victory,
    // 结算失败
    Defeat,
    // 重开提示
    RestartHint,
    // 重开按钮
    Restart,
    // 退出按钮
    Quit,
    // 语言切换按钮
    LanguageToggle,
    // 准备状态
    Ready
}
