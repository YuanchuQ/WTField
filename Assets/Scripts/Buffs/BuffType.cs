// 列出 demo 支持的所有 Buff 类型
// 表示 Buff 的 gameplay 分类
public enum BuffType
{
    // 按百分比治疗
    HealPercent,
    // 回满生命并短暂无敌
    FullHeal,
    // 提升移动速度
    MoveSpeed,
    // 提升子弹伤害
    Damage,
    // 提升射击频率
    FireRate,
    // 降低受到的伤害
    Shield,
    // 让子弹穿透敌人
    Pierce,
    // 让子弹产生爆炸伤害
    Explosive,
    // 吸引场上的拾取物
    Magnet
}
